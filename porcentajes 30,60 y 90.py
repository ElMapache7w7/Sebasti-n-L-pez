#solicitar al usuario un numero

numero = float (input("ingrese un numero: "))

#calcular el % del numero

porcentaje1 = 0.30 * numero

porcentaje2 = 0.60 * numero

porcentaje3  = 0.90 * numero

print (f"el 30% de {numero} es {porcentaje1}")
print (f"el 60% de {numero} es {porcentaje2}")
print (f"el 90% de {numero} es {porcentaje3}")
